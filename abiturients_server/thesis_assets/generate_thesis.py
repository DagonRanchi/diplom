def add_table(caption: str, headers: list[str], rows: list[list[str]]) -> None:
    global table_count
    table_count += 1
    add_para(f"Таблица {table_count} - {caption}", WD_ALIGN_PARAGRAPH.LEFT, indent=False)
    table = doc.add_table(rows=1, cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = "Table Grid"
    for idx, header in enumerate(headers):
        cell = table.rows[0].cells[idx]
        cell.text = header
    for row in rows:
        cells = table.add_row().cells
        for idx, value in enumerate(row):
            cells[idx].text = value
    for row in table.rows:
        for cell in row.cells:
            cell.vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.TOP
            for p in cell.paragraphs:
                p.alignment = WD_ALIGN_PARAGRAPH.LEFT
                set_paragraph_format(p, indent=False)
                for run in p.runs:
                    set_run_font(run, size=14)
    blank(1)


def add_figure(filename: str, caption: str) -> None:
    global figure_count
    figure_count += 1
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    set_paragraph_format(p, indent=False)
    run = p.add_run()
    run.add_picture(str(BW_DIR / filename), width=Cm(15), height=Cm(8))
    add_para(f"Рисунок {figure_count} - {caption}", WD_ALIGN_PARAGRAPH.CENTER, indent=False)
    blank(1)


def add_code(lines: list[str], max_lines: int = 42) -> None:
    for line in lines[:max_lines]:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.LEFT
        p.paragraph_format.space_before = Pt(0)
        p.paragraph_format.space_after = Pt(0)
        p.paragraph_format.line_spacing = 1
        p.paragraph_format.first_line_indent = Cm(0)
        run = p.add_run(line.rstrip("\n"))
        set_run_font(run, size=9)


def read_lines(relative: str, start: int = 1, count: int = 42) -> list[str]:
    path = ROOT / relative
    lines = path.read_text(encoding="utf-8").splitlines()
    prefix = [f"# Файл: {relative}", f"# Строки: {start}-{min(start + count - 1, len(lines))}"]
    return prefix + lines[start - 1 : start - 1 + count]


